package july_15.moremethod;

public class MoreMethod_Addition_Multiplication {


    public static int calculation ( int num1, int num2, int num3){
        int calculation = num1 + num2 + num3;
        return calculation;
    }

    public static int calculation ( int num1, int num2, int num3, int num4){
        int calculation = num1 * num2 * num3 * num4;
        return calculation;
    }
}